export function translate(
  locale: AppLocale,
  key: MessageKey,
  values: Record<string, string | number> = {}
) {
  const template = dictionaries[locale][key] ?? en[key] ?? key;
  return template.replace(/\{(\w+)\}/g, (_, name: string) => String(values[name] ?? `{${name}}`));
}

export function useT() {
  const locale = useUiStore((state) => state.locale);
  return (key: MessageKey, values?: Record<string, string | number>) =>
    translate(locale, key, values);
}

export function useProductName() {
  const locale = useUiStore((state) => state.locale);
  return productNameForLocale(locale);
}

export function localeTierLabel(locale: AppLocale, appLocale: AppLocale): string {
  const tier = localeInfo(locale).tier;
  if (tier === "core") return translate(appLocale, "locale.core");
  if (tier === "experimental") return translate(appLocale, "locale.experimental");
  return translate(appLocale, "locale.community");
}
